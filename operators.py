# Arithmetic	Basic math operations	+, -, *, /, %, //, **
# Two Value get from an input and perform all Arithmetic operators
a = int(input("Enter NUmber1: ")) 
b = int(input("Enter NUmber2: "))
print (f"{a} + {b} = {a+b}") #➕ + (Addition)
print (f"{a} - {b} = {a-b}")  #➖ - (Subtraction)
print (f"{a} * {b} = {a*b}") #✖️ * (Multiplication)
print (f"{a} / {b} = {a/b}") #➗ / (Division)
print (f"{a} % {b} = {a%b}") # % (Modulus)
print (f"{a} // {b} = {a//b}") # // (Floor Division) - Divides and returns only the integer part (floor value).
print (f"{a} ** {b} = {a**b}") # ** (Exponentiation) - Raises the left operand to the power of the right operand.

